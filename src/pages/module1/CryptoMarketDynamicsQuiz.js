import React, { useState } from 'react';

const CryptoMarketDynamicsQuiz = () => {
  const [currentQuestion, setCurrentQuestion] = useState(0);
  const [selectedAnswer, setSelectedAnswer] = useState(null);
  const [showResult, setShowResult] = useState(false);
  const [score, setScore] = useState(0);
  const [showExplanation, setShowExplanation] = useState(false);

  const updateProgress = (moduleId, sectionId, completed) => {
    console.log(`Progress updated: Module ${moduleId}, Section ${sectionId}, Completed: ${completed}`);
  };

  const quizQuestions = [
    {
      question: "What is market capitalization in cryptocurrency markets?",
      options: [
        "The total number of trades in 24 hours",
        "The current price of a cryptocurrency",
        "The total value of a cryptocurrency's circulating supply",
        "The number of exchanges listing the cryptocurrency"
      ],
      correctAnswer: 2,
      explanation: "Market capitalization is calculated by multiplying the current price of a cryptocurrency by its circulating supply, representing the total value of all existing tokens."
    },
    {
      question: "Which of these is NOT a key characteristic of cryptocurrency markets?",
      options: [
        "24/7 Trading",
        "High Volatility",
        "Centralized Control",
        "Global Accessibility"
      ],
      correctAnswer: 2,
      explanation: "Cryptocurrency markets are characterized by 24/7 trading, high volatility, and global accessibility. Centralized control is NOT a characteristic - in fact, decentralization is a key feature."
    },
    {
      question: "What is a 'bull market' in cryptocurrency?",
      options: [
        "A market with low trading volume",
        "A period of falling prices",
        "A period of generally rising prices",
        "A market with high volatility"
      ],
      correctAnswer: 2,
      explanation: "A bull market refers to a period where prices are generally rising, indicating positive market sentiment and upward price momentum."
    },
    {
      question: "What factor typically has the LEAST direct impact on cryptocurrency prices?",
      options: [
        "Regulatory news",
        "Market sentiment",
        "Weather conditions",
        "Technological developments"
      ],
      correctAnswer: 2,
      explanation: "While regulatory news, market sentiment, and technological developments directly affect cryptocurrency prices, weather conditions typically have minimal direct impact (except in cases where they affect mining operations)."
    },
    {
      question: "What is the primary purpose of technical analysis in cryptocurrency trading?",
      options: [
        "To predict future price movements using historical data",
        "To evaluate the team behind a project",
        "To assess regulatory compliance",
        "To measure market capitalization"
      ],
      correctAnswer: 0,
      explanation: "Technical analysis involves using historical price and volume data to identify patterns and predict future price movements, often using tools like moving averages and relative strength indicators."
    }
  ];

  const handleAnswerSelect = (optionIndex) => {
    setSelectedAnswer(optionIndex);
    setShowExplanation(true);
  };

  const moveToNextQuestion = () => {
    const isCorrect = selectedAnswer === quizQuestions[currentQuestion].correctAnswer;
    
    if (isCorrect) {
      setScore(prev => prev + 1);
    }

    if (currentQuestion < quizQuestions.length - 1) {
      setCurrentQuestion(prev => prev + 1);
      setSelectedAnswer(null);
      setShowExplanation(false);
    } else {
      setShowResult(true);
      const passThreshold = quizQuestions.length * 0.6;
      updateProgress(1, 'market-dynamics-quiz', score >= passThreshold);
    }
  };

  const restartQuiz = () => {
    setCurrentQuestion(0);
    setSelectedAnswer(null);
    setShowResult(false);
    setScore(0);
    setShowExplanation(false);
  };

  if (showResult) {
    return (
      <div className="container mx-auto px-4 py-8 max-w-2xl">
        <div className="bg-white shadow-lg rounded-lg p-8 text-center">
          <h2 className="text-3xl font-bold mb-4 text-blue-800">
            Quiz Completed!
          </h2>
          <p className="text-xl mb-4">
            You scored {score} out of {quizQuestions.length}
          </p>
          {score >= 3 ? (
            <div className="bg-green-100 border-l-4 border-green-500 p-4 mb-4">
              <p className="text-green-700">
                üéâ Congratulations! You've passed the Crypto Market Dynamics quiz!
              </p>
            </div>
          ) : (
            <div className="bg-red-100 border-l-4 border-red-500 p-4 mb-4">
              <p className="text-red-700">
                You didn't pass this time. Review the content and try again.
              </p>
            </div>
          )}
          <button 
            onClick={restartQuiz}
            className="bg-blue-500 text-white px-6 py-2 rounded hover:bg-blue-600"
          >
            Restart Quiz
          </button>
        </div>
      </div>
    );
  }

  const currentQuizQuestion = quizQuestions[currentQuestion];

  return (
    <div className="container mx-auto px-4 py-8 max-w-2xl">
      <div className="bg-white shadow-lg rounded-lg p-8">
        <div className="mb-6">
          <h2 className="text-2xl font-bold text-blue-800 mb-4">
            Crypto Market Dynamics Quiz
            <span className="text-sm ml-4 text-gray-600">
              Question {currentQuestion + 1} of {quizQuestions.length}
            </span>
          </h2>
          
          <div className="bg-blue-50 rounded-lg p-4 mb-6">
            <p className="text-lg text-gray-700">
              {currentQuizQuestion.question}
            </p>
          </div>

          <div className="grid md:grid-cols-2 gap-4">
            {currentQuizQuestion.options.map((option, index) => (
              <button
                key={index}
                onClick={() => handleAnswerSelect(index)}
                className={`
                  w-full p-4 rounded-lg text-left transition-all duration-300
                  ${selectedAnswer === null 
                    ? 'bg-gray-100 hover:bg-blue-100' 
                    : index === currentQuizQuestion.correctAnswer 
                      ? 'bg-green-200' 
                      : selectedAnswer === index 
                        ? 'bg-red-200' 
                        : 'bg-gray-100'}
                `}
                disabled={selectedAnswer !== null}
              >
                {option}
              </button>
            ))}
          </div>

          {showExplanation && (
            <div className={`
              mt-6 p-4 rounded-lg
              ${selectedAnswer === currentQuizQuestion.correctAnswer 
                ? 'bg-green-100 border-l-4 border-green-500' 
                : 'bg-red-100 border-l-4 border-red-500'}
            `}>
              <h3 className="font-bold mb-2">
                {selectedAnswer === currentQuizQuestion.correctAnswer 
                  ? '‚úÖ Correct!' 
                  : '‚ùå Incorrect'}
              </h3>
              <p>{currentQuizQuestion.explanation}</p>
            </div>
          )}

          {selectedAnswer !== null && (
            <button
              onClick={moveToNextQuestion}
              className="mt-6 w-full bg-blue-500 text-white py-3 rounded-lg hover:bg-blue-600"
            >
              {currentQuestion < quizQuestions.length - 1 
                ? 'Next Question' 
                : 'Finish Quiz'}
            </button>
          )}
        </div>
      </div>
    </div>
  );
};

export default CryptoMarketDynamicsQuiz;
